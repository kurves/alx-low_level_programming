/**
 * add - function to add elements
 * @a: first number
 * @b: second number
 * Description: function to add elements
 * Return: integer value
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * mod - function to mod elements
 * @a: first number
 * @b: second number
 * Description: function to add elements
 * Return: integer value
 */
int mod(int a, int b)
{
	return (a % b);
}
/**
 * sub - function to subtract elements
 * @a: first number
 * @b: second number
 * Description: function to add elements
 * Return: integer value
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * div - function to divide elements
 * @a: first number
 * @b: second number
 * Description: function to add elements
 * Return: integer value
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 * mul - function to multiply elements
 * @a: first number
 * @b: second number
 * Description: function to add elements
 * Return: integer value
 */

int mul(int a, int b)
{
	return (a * b);
}
